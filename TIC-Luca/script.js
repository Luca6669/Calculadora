
function suma(){
    let n1 = parseFloat(prompt("Dime un nombre"));
    let n2 = parseFloat(prompt("Dime un altre"));
    document.write(n1+n2)  
}
function resta(){
    let n1 = parseFloat(prompt("Dime un nombre"));
    let n2 = parseFloat(prompt("Dime un altre"));
    document.write(n1-n2)  
}
function mult(){
    let n1 = parseFloat(prompt("Dime un nombre"));
    let n2 = parseFloat(prompt("Dime un altre"));
    document.write(n1*n2)  
}
function div(){
    let n1 = parseFloat(prompt("Dime un nombre"));
    let n2 = parseFloat(prompt("Dime un altre"));
    document.write(n1/n2)  
}
function arrel(){
    let n1 = parseFloat(prompt("Dime un nombre"));
    document.write(Math.sqrt(n1))  
}